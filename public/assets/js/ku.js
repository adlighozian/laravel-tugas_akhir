function filtersatu() {
    var tsatu = document.getElementById("tsatu");
    var tdua = document.getElementById("tdua");
    var thsatu = document.getElementById("thsatu");
    var thdua = document.getElementById("thdua");
    tsatu.classList.remove("hidden");
    tdua.classList.add("hidden");
    thsatu.classList.remove("hidden");
    thdua.classList.add("hidden");
}
